    private void RefreshValues(Formation f) {
        tblStagFormModel.reset();
        tblECFModel.reset();
        for (Stagiaire s : f.getStagiaires()) {
            tblStagFormModel.add(s);
        }
        for (ECF e : f.getECFs()) {
            tblECFModel.add(e);
        }
    }

